    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loginActionPerformed
        // TODO add your handling code here:
        String username = txt_username.getText();
        String password = txt_password.getText();

        boolean isLoggedIn = false;

        //user = new User("asd", "asd");
        for (int i = 0; i < Data.userList.size(); i++) {

            if (Data.userList.get(i).username.equals(username) && Data.userList.get(i).password.equals(password)) {
                isLoggedIn = true;
                user = Data.userList.get(i);
            }
        }

        if (isLoggedIn) {
            try {

                socket = new Socket("localhost", 5000);
                client = new Client(socket, user);
                client.Listen();
                client.sendMessage(username);

                lst_messages.setModel(client.messageList);

                btn_createProejct.setEnabled(true);
                btn_joinChat.setEnabled(true);
                btn_send.setEnabled(true);
                txt_message.setEnabled(true);
                lst_project.setEnabled(true);
                txt_projectName.setEnabled(true);

                btn_login.setEnabled(false);
                txt_username.setEnabled(false);
                txt_password.setEnabled(false);

                for (int i = 0; i < user.projectList.size(); i++) {
                    project_list.addElement(user.projectList.get(i).projectKey + " -> " + user.projectList.get(i).name);
                }

            } catch (Exception e) {
                client.Close();
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "WRONG PASSWORD OR USERNAME");
        }

    }//GEN-LAST:event_btn_loginActionPerformed

