    private void btn_joinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_joinActionPerformed
        // TODO add your handling code here:
        String projectKey = txt_projectKeyJoin.getText();
        boolean isFound = false;

        Project p = null;

        for (int i = 0; i < Data.projectList.size(); i++) {
            if (projectKey.equals(Data.projectList.get(i).projectKey)) {
                p = Data.projectList.get(i);
                isFound = true;
                break;
            }
        }

        boolean isJoined = false;

        if (isFound) {

            for (int i = 0; i < p.userList.size(); i++) {
                if (p.userList.get(i) == this.user) {
                    isJoined = true;
                }
            }

            if (!isJoined) {
                p.userList.add(user);
                user.projectList.add(p);
                p.userListModel.addElement(user.username);

                project_list.addElement(p.name + " -> " + p.projectKey);
            } else {
                JOptionPane.showMessageDialog(rootPane, "YOU ALREADY JOINED THIS PROJECT");
            }

        } else {
            JOptionPane.showMessageDialog(rootPane, "WRONG PROJECT KEY");
        }


    }//GEN-LAST:event_btn_joinActionPerformed

